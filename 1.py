print("Парижская дива, Баронесса Де Синяк, проснулась ранним утром и задумалась... Выйти ли сегодня в свет или остаться дома?")

choice1 = input("Что выберет Баронесса? (остаться/улица) ").strip().lower()

if choice1 == "остаться":
    print("Баронесса решила остаться дома — расслабиться и провести утро в роскошной тишине своих апартаментов.")
    choice2 = input("Чем она займётся? (обзор/образы) ").strip().lower()

    if choice2 == "обзор":
        print("Баронесса записала утончённый обзор на капсульную коллекцию именитого бренда. Видео сразу стало вирусным.")
    elif choice2 == "образы":
        print("Баронесса придумала новые образы для будущих клипов и заказала платье ручной работы из Дубая. Съёмка обещает быть грандиозной.")
    else:
        print("Баронесса передумала и просто наслаждалась утренним кофе у окна.")

elif choice1 == "улица":
    print("Баронесса решила выйти в свет — Париж манит шоппингом и утренней роскошью.")
    choice2 = input("Куда направится Баронесса? (гермес/подружка) ").strip().lower()

    if choice2 == "гермес":
        print("Баронесса отправилась в бутик Hermès на охоту за новой сумкой Birkin. Возможно, сегодня удача будет на её стороне.")
    elif choice2 == "подружка":
        print("Баронесса поехала на шоппинг со своей подругой Анкорской. Впереди — прогулки, покупки и бесконечные разговоры о моде.")
    else:
        print("Баронесса передумала и вернулась домой, чтобы досмотреть любимую оперу.")

else:
    print("Баронесса так и не решила, чем заняться, и осталась дома отдыхать в своём шелковом пенюаре.")
